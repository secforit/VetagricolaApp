    execute format('drop policy if exists %I on public.%I', scoped_table || '_insert_member', scoped_table);
    execute format(
      'create policy %I on public.%I for insert with check (public.user_can_access_clinic(clinic_id))',
      scoped_table || '_insert_member',
      scoped_table
    );
    execute format('drop policy if exists %I on public.%I', scoped_table || '_update_member', scoped_table);
    execute format(
      'create policy %I on public.%I for update using (public.user_can_access_clinic(clinic_id)) with check (public.user_can_access_clinic(clinic_id))',
      scoped_table || '_update_member',
      scoped_table
    );
    execute format('drop policy if exists %I on public.%I', scoped_table || '_delete_member', scoped_table);
    execute format(
      'create policy %I on public.%I for delete using (public.user_can_access_clinic(clinic_id))',
      scoped_table || '_delete_member',
      scoped_table
    );
  end loop;
