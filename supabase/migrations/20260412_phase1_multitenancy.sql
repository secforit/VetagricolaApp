  scoped_table text;
begin
  foreach scoped_table in array ARRAY[
    'clients',
    'pets',
    'vets',
    'appointments',
    'records',
    'prescriptions',
    'reminders',
    'sales'
  ]
  loop
    execute format('alter table public.%I enable row level security', scoped_table);
    execute format('drop policy if exists %I on public.%I', scoped_table || '_select_member', scoped_table);
    execute format(
      'create policy %I on public.%I for select using (public.user_can_access_clinic(clinic_id))',
