create or replace function public.user_is_member(target_clinic uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.user_clinics uc
    where uc.user_id = auth.uid()
      and uc.clinic_id = target_clinic
  );
$$;

create or replace function public.user_is_clinic_admin(target_clinic uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.user_clinics uc
    where uc.user_id = auth.uid()
      and uc.clinic_id = target_clinic
      and uc.role = 'clinic_admin'
  );
$$;

create or replace function public.user_can_access_clinic(target_clinic uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.user_clinics uc
    join public.clinics c on c.id = uc.clinic_id
    where uc.user_id = auth.uid()
      and uc.clinic_id = target_clinic
      and (
        c.status in ('trial', 'active')
        or (
          c.status = 'past_due'
          and c.grace_end > timezone('utc', now())
        )
      )
  );
$$;

create or replace function public.clinic_trial_user_cap_available(target_clinic uuid)
returns boolean
language sql
stable
as $$
  select case
    when not exists (
      select 1
      from public.clinics c
      where c.id = target_clinic
        and c.status = 'trial'
    ) then true
    else (
      select
        (
          select count(*)
          from public.user_clinics uc
          where uc.clinic_id = target_clinic
        ) < coalesce(
          (
            select cs.trial_user_limit
            from public.clinic_settings cs
            where cs.clinic_id = target_clinic
          ),
          2
        )
    )
  end;
$$;

